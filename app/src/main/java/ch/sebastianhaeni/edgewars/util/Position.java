package ch.sebastianhaeni.edgewars.util;

public class Position {
    private float mX;
    private float mY;

    public Position(float x, float y) {
        this.mX = x;
        this.mY = y;
    }

    public float getX() {
        return mX;
    }

    public float getY() {
        return mY;
    }
}
